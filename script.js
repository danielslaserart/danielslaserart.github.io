function renderGalleryPage() {
  const container = document.getElementById('dynamic-gallery');
  if (!container) return;

  const params = new URLSearchParams(window.location.search);
  const key = params.get('album') || 'adler';
  const data = galleryData[key] || galleryData.adler;
  const tones = ['tone-1', 'tone-2', 'tone-3', 'tone-4', 'tone-5', 'tone-6'];

  document.title = `${data.title} | Daniels Laser Art`;

  const galleryItems = data.items.map((item, index) => `
    <article class="gallery-item">
      <div class="gallery-image ${tones[index % tones.length]}">
        <span>Bild ${String(index + 1).padStart(2, '0')}</span>
      </div>
      <div class="gallery-caption">
        <h3>${item[0]}</h3>
        <p>${item[1]}</p>
      </div>
    </article>
  `).join('');

  container.innerHTML = `
    <div class="gallery-layout">
      <aside class="profile-card">
        <div class="avatar">DLA</div>
        <div class="cover-chip">${data.badge}</div>
        <h2 style="margin-top: 14px;">${data.title}</h2>
        <p>${data.subtitle}</p>
        <div class="stat-row">
          <div class="stat-box"><strong>${data.stats.posts}</strong><span>Posts</span></div>
          <div class="stat-box"><strong>${data.stats.likes}</strong><span>Likes</span></div>
          <div class="stat-box"><strong>${data.stats.saves}</strong><span>Saves</span></div>
        </div>
        <a class="btn btn-primary" href="index.html#galerie">Zurück zum Feed</a>
      </aside>

      <section class="gallery-shell">
        <div class="breadcrumbs">
          <a href="index.html">Startseite</a>
          <span>•</span>
          <span>${data.title}</span>
        </div>
        <div class="gallery-header">
          <div>
            <h1 class="gradient-text" style="font-size: clamp(2rem, 4vw, 3.5rem);">${data.title}</h1>
            <p>${data.subtitle}</p>
          </div>
          <a class="btn btn-secondary" href="index.html#kontakt">Anfrage senden</a>
        </div>
        <div class="gallery-grid">${galleryItems}</div>
      </section>
    </div>
  `;
}

document.addEventListener('DOMContentLoaded', renderGalleryPage);
